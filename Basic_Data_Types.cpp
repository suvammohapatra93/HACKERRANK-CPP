
#include <bits/stdc++.h>

using namespace std;

int main()
{
    int a;
    long b;
    long long c;
    char d;
    float e;
    double f;
    cin >> a >> b >> c >> d >> e >> f;
    cout << a << endl
         << b << endl
         << c << endl
         << d << endl;
    printf("%f\n%lf\n", e, f);
    return 0;
}

//! Time Complexity = O(1)
//! Space Complexity = O(1)